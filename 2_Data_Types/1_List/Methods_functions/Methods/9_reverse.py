"""
The reverse() method reverses the element of a list.
This method doesn't return any value

Syntax:
list.reverse()
"""

programming_languages = ["C", "C++", "Java", "Python", "Kotlin"]
print("Original list : ", programming_languages)
result = programming_languages.reverse()
print("Updated list  : ", programming_languages)
print(f"Return Value  : {result}")