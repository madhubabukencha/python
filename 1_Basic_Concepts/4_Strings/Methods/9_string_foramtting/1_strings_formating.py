# For formatting characters
print("My grade is:%c" % 'C')

# For strings
print("My name is:%s" % 'Madhu Babu')

# %d and %i used for formatting signed decimals
print("Kurnool temp is %dc" % (-2))

# Formatting unsigned decimals
print("Tomorrow temperature in kurnool %uc" % 10)

# To format float numbers
print("My rating is:%.10f" % 2.660000)

# If don't give space then it will treat it as space
print("My rating is:%20f" % 2.660000)
